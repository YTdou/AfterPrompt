function hasTranslation(value: string): boolean {
  return byValue.has(value)
    || englishAliases.has(value)
    || translations.some((entry) => value.includes(entry.zh) || value.includes(entry.en));
}

function preserveWhitespace(source: string, translated: string): string {
  const match = source.match(/^(\s*)([\s\S]*?)(\s*)$/);
  return match ? `${match[1]}${translated}${match[3]}` : translated;
}

export function detectUiLocale(languages?: readonly string[]): UiLocale {
  const browserLanguages = globalThis.navigator?.languages;
  const candidates = languages ?? (browserLanguages?.length ? browserLanguages : [globalThis.navigator?.language ?? ""]);
  return candidates.some((language) => language.toLowerCase().startsWith("zh")) ? "zh-CN" : "en";
}

export function translateUiText(value: string, locale: UiLocale): string {
  const translation = byValue.get(value) ?? englishAliases.get(value);
  if (translation) return translation[locale === "zh-CN" ? "zh" : "en"];
  const dynamicText = locale === "en"
    ? value.replace(
      /^(.+?) 属于 Build (\d+)，但可见祖先 (.+?) 要到 Build (\d+) 才出现。$/,
      "$1 belongs to Build $2, but visible ancestor $3 does not appear until Build $4.",
    ).replace(/必须是 (.+?) 之一(?=；|\n|$)/g, (_match, options: string) => `Must be one of ${options.replaceAll("、", ", ")}`)
    : value.replace(
      /^(.+?) belongs to Build (\d+), but visible ancestor (.+?) does not appear until Build (\d+)\.$/,
      "$1 属于 Build $2，但可见祖先 $3 要到 Build $4 才出现。",
    ).replace(/Must be one of (.+?)(?=;|\n|$)/g, (_match, options: string) => `必须是 ${options.replaceAll(", ", "、")} 之一`);
  const replacements = locale === "zh-CN" ? chineseReplacements : englishReplacements;
  return replacements.reduce(
    (result, entry) => locale === "zh-CN"
      ? result.replaceAll(entry.en, entry.zh)
      : result.replaceAll(entry.zh, entry.en),
    dynamicText,
  );
}

export class UiLocalizer {
  private bindings: Array<{ node: Text | Element; attribute?: string; source: string }> = [];
  private readonly boundTextNodes = new WeakSet<Text>();
  private readonly boundAttributes = new WeakMap<Element, Set<string>>();

  constructor(private currentLocale: UiLocale = detectUiLocale()) {
    this.applyDocumentLanguage();
  }

  get locale(): UiLocale {
    return this.currentLocale;
  }

  t(value: string): string {
    return translateUiText(value, this.currentLocale);
  }

  setLocale(locale: UiLocale): void {
    this.currentLocale = locale;
    this.applyDocumentLanguage();
    this.refresh();
  }

  bind(root: ParentNode): void {
    const textWalker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT);
    let textNode = textWalker.nextNode();
    while (textNode) {
      const source = textNode.nodeValue ?? "";
      const parent = textNode.parentElement;
      const node = textNode as Text;
      if (!parent?.closest("[data-l10n-skip], #canvas-host, #code-editor") && !this.boundTextNodes.has(node) && hasTranslation(source.trim())) {
        this.boundTextNodes.add(node);
        this.bindings.push({ node, source });
      }
      textNode = textWalker.nextNode();
    }
    const elements = root instanceof Element ? [root, ...Array.from(root.querySelectorAll("*"))] : Array.from(root.querySelectorAll("*"));
    for (const element of elements) {
      if (element.closest("[data-l10n-skip], #canvas-host, #code-editor")) continue;
      for (const attribute of ["aria-label", "title", "placeholder", "data-label", "alt"]) {
        const source = element.getAttribute(attribute);
        const bound = this.boundAttributes.get(element) ?? new Set<string>();
        if (source && !bound.has(attribute) && hasTranslation(source.trim())) {
          bound.add(attribute);
          this.boundAttributes.set(element, bound);
          this.bindings.push({ node: element, attribute, source });
        }
      }
    }
    this.refresh();
  }

  refresh(): void {
    this.bindings = this.bindings.filter((binding) => binding.node.isConnected);
    for (const binding of this.bindings) {
      const translated = preserveWhitespace(binding.source, this.t(binding.source.trim()));
      if (binding.node instanceof Text) binding.node.nodeValue = translated;
      else if (binding.attribute) binding.node.setAttribute(binding.attribute, translated);
    }
  }

  private applyDocumentLanguage(): void {
    document.documentElement.lang = this.currentLocale;
  }
}
